    private void searchFlightBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchFlightBtnActionPerformed
       try {

            String Arrival = selectArrival.getSelectedItem().toString();
            String Departure = selectDeparture.getSelectedItem().toString();
           
            DateFormat df = new SimpleDateFormat("dd-MM-YYYY");
            String DOt = df.format(selectDot.getDate());

            if (Arrival.equalsIgnoreCase(Departure)) {
                JOptionPane.showMessageDialog(this, " Error Arrival and Departure are same ");
               
                selectArrival.setSelectedItem(null);
                
                selectDeparture.setSelectedItem(null);
                
                selectDot.setDate(null);
            } else {
                Class.forName("com.mysql.jdbc.Driver");

                con = DriverManager.getConnection("jdbc:mysql://localhost/flight_booking", "root", "Shamshad@1");
                pre = con.prepareStatement("Select * from addflight where Arival=? and Departure=? and TravelDate=?");
                
                pre.setString(1, Arrival);
                pre.setString(2, Departure);
                pre.setString(3, DOt);
                

                ResultSet rs=pre.executeQuery();
                ResultSetMetaData rsm=rs.getMetaData();
                
                int c=rsm.getColumnCount();
                
                DefaultTableModel DF=(DefaultTableModel)tabelFlightData.getModel();
                DF.setRowCount(0);
                
                while(rs.next()){
                    Vector v=new Vector();
                    for(int i=0;i<=c;i++){
                         
                        v.add(rs.getString("FlightId"));
                        v.add(rs.getString("Flight_name"));
                        v.add(rs.getString("Arival"));
                        v.add(rs.getString("Departure"));
                        v.add(rs.getString("TravelDate"));
                        v.add(rs.getString("ArrivalTime"));
                        v.add(rs.getString("DepartureTime"));
                        v.add(rs.getString("duration"));
                        v.add(rs.getString("charge"));
                    }
                    DF.addRow(v);
                }
            }

        } catch (ClassNotFoundException ex) {
            Logger.getLogger(BookFlight.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(BookFlight.class.getName()).log(Level.SEVERE, null, ex);
        }
               
    }//GEN-LAST:event_searchFlightBtnActionPerformed

