    private void priceCalculationBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_priceCalculationBtnActionPerformed
        int price=Integer.parseInt(txtFair.getText());
        String CT=selectClassOfTravel.getSelectedItem().toString();
        
        if(CT.equalsIgnoreCase("Economic")){
            price+=1500;
            txtFair.setText(String.valueOf(price));
        }else if(CT.equalsIgnoreCase("First")){
            price+=2200;
            txtFair.setText(String.valueOf(price));
        }
        int seats=Integer.parseInt(selectSeat.getValue().toString());
        
        int cost=price*seats;
        txtTotalPrice.setText(String.valueOf(cost));
    }//GEN-LAST:event_priceCalculationBtnActionPerformed


