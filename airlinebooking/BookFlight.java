    public void AutoId() {
        try {
            Class.forName("com.mysql.jdbc.Driver");

            con = DriverManager.getConnection("jdbc:mysql://localhost/flight_booking", "root", "Shamshad@1");
            pre = con.prepareStatement("Select MAX(TicketId) from ticket");
            ResultSet rs = pre.executeQuery();
            rs.next();
            if (rs.getString("MAX(TicketId)") == null) {
                autoTicketId.setText("T001");
            } else {
                int idLast = Integer.parseInt(rs.getString("MAX(TicketId)").substring(1, rs.getString("MAX(TicketId)").length()));
                idLast++;
                autoTicketId.setText("T" + String.format("%03d", idLast));
            }

        } catch (ClassNotFoundException ex) {
            Logger.getLogger(BookFlight.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(BookFlight.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

